package OrientacaoObjetosPratic;

public class DevFullStack {
    private String nome;
    private String cpf;
    private String frontEnd;
    private String bancoDeDados;
    private double salario;

    public DevFullStack() {
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getFrontEnd() { return frontEnd; }

    public void setFrontEnd(String frontEnd) { this.frontEnd = frontEnd; }

    public String getBancoDeDados() { return bancoDeDados; }

    public void setBancodeDados(String bancoDeDados) { this.bancoDeDados = bancoDeDados; }

    public double getSalario() {
        return salario;
    }

    public void setSalario(double salario) {
        this.salario = salario;
    }
}
